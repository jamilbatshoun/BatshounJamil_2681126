#include <iostream>

using namespace std;

int main(int argc, char** argv) {
    
    //Create three variables to store the integers 50 and 100 and their sum.
    int x, y, total;
    
    //Determine the value of the three variables.
    x=50;
    y=100;
    total=x+y;
    
    //Output the sum of x and y.
    cout<<" The sum of 50 and 100 is "<<total<<"."<<endl;
    
    
    return 0;
}

